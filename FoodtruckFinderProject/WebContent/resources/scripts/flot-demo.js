//Flot Pie Chart Female
$(function() {

    var data = [{
        label: "5points",
        data: 10
    }, {
        label: "4points",
        data: 30
    }, {
        label: "3points",
        data: 20
    }, {
        label: "2points",
        data: 5
    },  {
        label: "1point",
        data: 1
    }];
    

    var plotObj = $.plot($("#flot-pie-chart-F"), data, {
        series: {
            pie: {
                show: true
            }
        },
        grid: {
            hoverable: true
        },
        tooltip: true,
        tooltipOpts: {
            content: "%p.0%, %s", // show percentages, rounding to 2 decimal places
            shifts: {
                x: 20,
                y: 0
            },
            defaultTheme: false
        }
    });

});


    
